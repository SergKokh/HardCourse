package Variables;

public class VariablesDouble {
    double a = 50.50;
    double b = 20.20;
    double c = 10.30;

    public double summa(){
        double s = a + b + c;
        return s;
    }
    public double difference(){
        double d = a - b - c;
        return d;
    }
    public double multi(){
        double m = a * b * c;
        return m;
    }
    public double division(){
        double div = a / b;
        return div;
    }
}
