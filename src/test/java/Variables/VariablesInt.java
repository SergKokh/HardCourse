package Variables;

public class VariablesInt {

    int a = 50;
    int b = 20;
    int c = 10;

    public int summa(){
        int s = a + b + c;
        return s;
    }
    public int difference(){
        int d = a - b - c;
        return d;
    }
    public int milti(){
        int m = a * b * c;
        return m;
    }
    public int division(){
        int div = a / b;
        return div;
    }
}
